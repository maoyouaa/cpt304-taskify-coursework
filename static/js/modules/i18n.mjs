export function getStoredLocale(storage) {
  const candidate = storage?.getItem?.(STORAGE_KEYS.locale);
  return candidate && translations[candidate] ? candidate : DEFAULT_LOCALE;
}

export function setStoredLocale(storage, locale) {
  if (translations[locale]) {
    storage?.setItem?.(STORAGE_KEYS.locale, locale);
  }
}

export function t(locale, key) {
  return translations[locale]?.[key] || translations[DEFAULT_LOCALE][key] || key;
}

export function translatePage(documentRef, locale) {
  documentRef.documentElement.lang = locale;

  documentRef.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(locale, node.dataset.i18n);
  });

  documentRef.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.setAttribute("placeholder", t(locale, node.dataset.i18nPlaceholder));
  });

  documentRef.querySelectorAll("[data-lang-switch]").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.langSwitch === locale);
    button.setAttribute("aria-pressed", String(button.dataset.langSwitch === locale));
  });
}
